    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        estadisticas cal = new estadisticas();
        double n1 = Double.valueOf(jtf1.getText()).doubleValue();
        double n2 = Double.valueOf(jtf2.getText()).doubleValue();
        String res = "";

        if (jRadioButton1.isSelected()) {
            res = String.valueOf(cal.sumar(n1, n2));
        } else if (jRadioButton2.isSelected()) {
            res = String.valueOf(cal.restar(n1, n2));
        } else if (jRadioButton3.isSelected()) {
            res = String.valueOf(cal.multiplicar(n1, n2));
        } else if (jRadioButton4.isSelected()) {
            res = String.valueOf(cal.dividir(n1, n2));
        } else if (jRadioButton5.isSelected()) {
            res = String.valueOf(cal.promedio(n1, n2));
        } else if (jRadioButton6.isSelected()) {
            res = String.valueOf(cal.mayor(n1, n2));
        } else if (jRadioButton7.isSelected()) {
            res = String.valueOf(cal.menor(n1, n2));
        }
        jLabel3.setText("Respuesta: " + res);
        if(res.length()!=0){
            DefaultListModel modelito =(DefaultListModel) jList1.getModel();
            modelito.addElement(res);
            jList1.setModel(modelito);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

