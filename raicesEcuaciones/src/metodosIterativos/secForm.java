    private void jTFToleranciaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFToleranciaKeyPressed
        if ((jTFTolerancia.getText().contains(".") && evt.getKeyChar() == '.') && !Character.isDigit(evt.getKeyChar())) {
            evt.consume();
        }
}//GEN-LAST:event_jTFToleranciaKeyPressed

