    private void jTFToleranciaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFToleranciaKeyTyped
        if ((!Character.isDigit(evt.getKeyChar()) || jTFTolerancia.getText().length() > 15) && jTFTolerancia.getText().contains(".")) {
            evt.consume();
        }
}//GEN-LAST:event_jTFToleranciaKeyTyped

    private void cmbCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCalcularActionPerformed
        try {
            jTFRaiz.setText("");
            objSecante = new Secante(Double.parseDouble(jTFa.getText()), Double.parseDouble(jTFb.getText()), Double.parseDouble(jTFTolerancia.getText()), Integer.parseInt(jTFn.getText()));
            objSecante.setModelo(modeloTabla);
            jTFRaiz.setText(String.format("%.6f", objSecante.calcularRaiz())); //trabajamos con 6 decimales después del punto
            lblErrores.setText("");
        } catch (Exception e) {
            lblErrores.setText(e.toString());
        }
}//GEN-LAST:event_cmbCalcularActionPerformed

