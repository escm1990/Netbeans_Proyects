    private void cmbCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCalcularActionPerformed
        try {
            jTFRaiz.setText("");
            objBiseccion = new Biseccion(Double.parseDouble(jTFa.getText()), Double.parseDouble(jTFb.getText()), Double.parseDouble(jTFTolerancia.getText()), Integer.parseInt(jTFn.getText()));
            objBiseccion.setModelo(modeloTabla);

            jTFRaiz.setText(String.format("%.6f", objBiseccion.calcularRaiz())); //trabajamos con 6 decimales despu√©s del punto
            lblErrores.setText("");
        } catch (Exception e) {
            lblErrores.setText(e.toString());
        }
}//GEN-LAST:event_cmbCalcularActionPerformed

