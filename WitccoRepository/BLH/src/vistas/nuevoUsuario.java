private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        if (bgAdm.isSelected(null) || jtfNombreUsuario.getText().equals("") || jpfContraseña.getText().equals("") || jpfRepitaContra.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "¡Por favor, complete todos los datos!");
        } else {
            if (jpfContraseña.getText().equals(jpfRepitaContra.getText())) {

                opU.nuevoUsuario(jtfNombreUsuario.getText(), jpfContraseña.getText(), adm);
                String usu = opU.usuarioUp();
                opU.setEstado(usu, "Agrego usuario " + jtfNombreUsuario.getText());
                jtfNombreUsuario.setText("");
                jpfContraseña.setText("");
                jpfRepitaContra.setText("");
                new InstanceOfGeneralidades().admonUser.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
                jpfContraseña.setText("");
                jpfRepitaContra.setText("");
            }
        }
    } catch (Exception e) {
    }
}//GEN-LAST:event_jButton1ActionPerformed

