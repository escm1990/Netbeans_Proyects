    private void jbGuardarEncargadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarEncargadoActionPerformed
        // TODO add your handling code here:
        try {
            if (jtEncargado.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(rootPane, "¡Seleccione Encargado para Almacenar!");
            } else if (jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 0).equals("") || jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 1).equals("") || jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 2).equals("") || jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 3).equals("") || jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 4).equals("") || jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 5).equals("")) {
                JOptionPane.showMessageDialog(rootPane, "¡Por favor, complete datos del Encargado!");
            } else {
                Encargado encargado = new Encargado(jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 0).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 1).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 2).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 3).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 4).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 5).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 6).toString());
                op.almacenarEncargado(encargado);
                this.jbNuevoEncargado.setEnabled(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al almacenar: " + e.getMessage());
        }
    }//GEN-LAST:event_jbGuardarEncargadoActionPerformed

