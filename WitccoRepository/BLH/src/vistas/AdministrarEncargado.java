    private void jbEliminarEncargadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarEncargadoActionPerformed
        // TODO add your handling code here:
        try {
            if (jtEncargado.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Seleccione encargado a eliminar!");
            } else {
                int msj = JOptionPane.showConfirmDialog(rootPane, "¿En verdad desea eliminar?", "Confirmación", JOptionPane.YES_NO_OPTION);
                if (msj == 0) {
                    if (opU.isAdmin()) {
                        usu = opU.usuarioUp();
                        opU.setEstado(usu, "Elimino encargado");
                        Encargado encargado = new Encargado(jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 0).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 1).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 2).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 3).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 4).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 5).toString(), jtEncargado.getValueAt(jtEncargado.getSelectedRow(), 6).toString());
                        op.eliminarEncargado(encargado);
                        DefaultTableModel model = (DefaultTableModel) jtEncargado.getModel();
                        model.removeRow(jtEncargado.getSelectedRow());
                    } else {
                        JOptionPane.showMessageDialog(null, "Lo siento, usted no esta autorizado para realizar esta acción\nsolo un administrador puede hacerlo", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jbEliminarEncargadoActionPerformed

