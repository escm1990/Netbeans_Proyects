    public void comoSalir() {
        int var = Integer.parseInt(jlIndiceOculto.getText());
        switch (var) {
            case 1:
                new ControlPaciente().setVisible(true);
                this.setVisible(false);
                break;
            case 2:
                new SeleccionActividad().setVisible(true);
                this.setVisible(false);
                break;

        }
    }

