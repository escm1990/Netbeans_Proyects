private void jbModificar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificar2ActionPerformed
    // TODO add your handling code here:

    try {
        if (jtVehiculos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(rootPane, "¡Seleccione vehiculo para almacenar!");
        } else if (jtVehiculos.getValueAt(jtVehiculos.getSelectedRow(), 0).equals("") || jtVehiculos.getValueAt(jtVehiculos.getSelectedRow(), 1).equals("")) {
            JOptionPane.showMessageDialog(rootPane, "¡Por favor, complete datos del Vehículo!");
        } else {
            Vehiculo vehiculo = new Vehiculo(jtVehiculos.getValueAt(jtVehiculos.getSelectedRow(), 0).toString(), jtVehiculos.getValueAt(jtVehiculos.getSelectedRow(), 1).toString());
            op.almacenarVehiculo(vehiculo);
            this.jbNuevoVehiculo.setEnabled(true);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Error al almacenar: " + e.getMessage());
    }
}//GEN-LAST:event_jbModificar2ActionPerformed

