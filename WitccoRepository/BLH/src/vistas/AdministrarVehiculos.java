private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
    try {
        if (jtVehiculos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione Vehículo a eliminar!");
        } else {
            int msj = JOptionPane.showConfirmDialog(rootPane, "¿En verdad desea eliminar?", "Confirmación", JOptionPane.YES_NO_OPTION);
            if (msj == 0) {
                if (opU.isAdmin()) {
                    String V = jtVehiculos.getValueAt(jtVehiculos.getSelectedRow(), 0).toString();
                    usu = opU.usuarioUp();
                    opU.setEstado(usu, "El. Vehiculo " + V);
                    Vehiculo carro = new Vehiculo(jtVehiculos.getValueAt(jtVehiculos.getSelectedRow(), 0).toString(), jtVehiculos.getValueAt(jtVehiculos.getSelectedRow(), 1).toString());
                    op.eliminarVehiculo(carro);
                    DefaultTableModel model = (DefaultTableModel) jtVehiculos.getModel();
                    model.removeRow(jtVehiculos.getSelectedRow());

                } else {
                    JOptionPane.showMessageDialog(null, "Lo siento, usted no esta autorizado para realizar esta acción\nsolo un administrador puede hacerlo", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Error: " + e.getMessage());
    }

}//GEN-LAST:event_jbEliminarActionPerformed

