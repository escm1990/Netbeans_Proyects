private void jtVehiculosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtVehiculosKeyReleased
    int key = evt.getKeyChar();

    System.out.println("FILA: " + filaAnterior);
    if (key == KeyEvent.VK_TAB && !jtVehiculos.getValueAt(filaAnterior, 0).equals("") && !jtVehiculos.getValueAt(filaAnterior, 1).equals("")) {
        Vehiculo vehiculo = new Vehiculo(jtVehiculos.getValueAt(filaAnterior, 0).toString(), jtVehiculos.getValueAt(filaAnterior, 1).toString());
        op.almacenarVehiculo(vehiculo);
        this.jbNuevoVehiculo.setEnabled(true);
    }}//GEN-LAST:event_jtVehiculosKeyReleased

