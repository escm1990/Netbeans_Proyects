    public void calcularEdad(Date fecha) {
        try {
            java.util.Date hoy = new Date(); 
            int anio = ((hoy.getYear() +1900) - (fecha.getYear()+1900));//Fecha de hoy 
            int meses = ((hoy.getMonth() +1) - (fecha.getMonth()+1));
            int d = (hoy.getDate() - fecha.getDate());
            jlEdad.setText(anio + " A単os "+meses +" Meses " +d +" Dias");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

