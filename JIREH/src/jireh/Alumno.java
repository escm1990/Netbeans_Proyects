    private void jbInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInsertarActionPerformed
        // TODO add your handling code here:
        try {
            String id = jftfID.getText();
            String foto = jtfURLFOTO.getText();
            String nom = jtfNombre.getText();
            String ape = jtfApellidos.getText();
            String dir = jtfDir.getText();
            String fec = jftfFecha.getText();
            String pme = jtfP_M_E.getText();
            String tel = jftfTELEFONO.getText();
            String obs = jtaObservaciones.getText();
            obj.agregarAlumno(id, foto, nom, ape, dir, fec, pme, tel, obs);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex.getMessage());
        }
       
    }//GEN-LAST:event_jbInsertarActionPerformed

