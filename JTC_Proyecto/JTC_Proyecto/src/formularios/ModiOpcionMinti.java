    private void jbnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnBuscarActionPerformed
        // TODO add your handling code here:
        String num = jtfbuscar.getText() + ":";
        oper.actualizarOpcnionMult(num);
        jtfNumPregunta.setText(oper.numero);
        jftPregunta.setText(oper.pregun);
        jftRespuesta.setText(oper.rep1);
        jftRespuesta1.setText(oper.rep2);
        jftRespuesta2.setText(oper.rep3);
        jftRespuesta3.setText(oper.rep4);
        jftRespuesta4.setText(oper.rep5);
        jlicomod.setVisible(false);
        jlmensajemodificar.setText("");
    }//GEN-LAST:event_jbnBuscarActionPerformed

    private void jbnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnModificarActionPerformed
        // TODO add your handling code here:
        String num, pre, resp1, resp2, resp3, resp4, resp5;
        num = jtfNumPregunta.getText();
        pre = jftPregunta.getText();
        resp1 = jftRespuesta.getText();
        resp2 = jftRespuesta1.getText();
        resp3 = jftRespuesta2.getText();
        resp4 = jftRespuesta3.getText();
        resp5 = jftRespuesta4.getText();
        try {
            oper.actualizarOpcionMultiple(num, pre, resp1, resp2, resp3, resp4, resp5);
            jlicomod.setVisible(true);
            jlmensajemodificar.setText("Modificación aceptada.");
        } catch (Exception e) {
            e.getMessage();
        }
}//GEN-LAST:event_jbnModificarActionPerformed

