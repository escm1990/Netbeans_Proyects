        private void JBagregarOpcionMultipleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBagregarOpcionMultipleActionPerformed
            int n = (Integer) jSpOP.getValue();
            if (this.contador < n) {
                this.contador = this.contador + 1;
                jlContador.setText(String.valueOf(this.contador) + ":");
                String pre = this.JTOpcionMPregunta.getText();
                String res = this.JTrespuestasOpcionMultiple.getText();
                String res1 = this.JTrespuestasOpcionMultiple1.getText();
                String res2 = this.JTrespuestasOpcionMultiple2.getText();
                String res3 = this.JTrespuestasOpcionMultiple3.getText();
                String res4 = this.JTrespuestasOpcionMultiple4.getText();
                String num = jlContador.getText();
                try {
                    op.agregarOpcionMulti(pre, res, res1, res2, res3, res4, num);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Error " + ex.getMessage());
                }
                if (this.contador < n) {
                    jlContador.setText(String.valueOf(this.contador + 1) + ":");
                }
                this.JTOpcionMPregunta.setText("");
                this.JTrespuestasOpcionMultiple.setText("");
                this.JTrespuestasOpcionMultiple1.setText("");
                this.JTrespuestasOpcionMultiple2.setText("");
                this.JTrespuestasOpcionMultiple3.setText("");
                this.JTrespuestasOpcionMultiple4.setText("");
                if (this.contador == n) {
                    this.contador = 0;
                    jSpOP.setValue(1);
                    jlContador.setText(" - ");
                    JBagregarOpcionMultiple.setEnabled(false);
                }
            }
            jSpOP.setEnabled(false);
}//GEN-LAST:event_JBagregarOpcionMultipleActionPerformed

