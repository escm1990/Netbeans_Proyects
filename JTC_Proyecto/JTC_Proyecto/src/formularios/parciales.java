    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        // TODO add your handling code here:
        try {
            String nombre = this.jTFNombreDParcial.getSelectedItem().toString();
            String fech = this.jTFecha.getText();
            String mat = this.jTFMateria.getText();
            String grupo = this.jTFGrupoTeorico.getText();
            String dep = this.jTFDeparmento.getText();
            String doc = this.jFTNombreDocente.getText();
            String tip = tipo;
            op.agregarEncabezado(nombre, fech, mat, grupo, dep, doc, tip);
//            this.mostrarEditorParcial();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex.getMessage());
            jBAgregar.setEnabled(true);
        }
        if(op.verificar == 1){
        jBAgregar.setEnabled(false);
        jBCancelar.setEnabled(false);
        jFTNombreDocente.setEnabled(false);
        jTFDeparmento.setEnabled(false);
        jTFNombreDParcial.setEnabled(false);
        jTFecha.setEnabled(false);
        jTFGrupoTeorico.setEnabled(false);
        jTFMateria.setEnabled(false);
        jccCalendario.setEnabled(false);
        this.mostrarEditorParcial();
        }else{
            jBAgregar.setEnabled(true);
        jBCancelar.setEnabled(true);
        jFTNombreDocente.setEnabled(true);
        jTFDeparmento.setEnabled(true);
        jTFNombreDParcial.setEnabled(true);
        jTFecha.setEnabled(true);
        jTFGrupoTeorico.setEnabled(true);
        jTFMateria.setEnabled(true);
        jccCalendario.setEnabled(true);
        }
    }//GEN-LAST:event_jBAgregarActionPerformed

