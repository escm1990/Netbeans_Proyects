    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        fila = tabla.rowAtPoint(evt.getPoint());
        if (fila > -1){
            this.cu = String.valueOf( tabla.getValueAt(fila, 0) );
            txtNota1.setText( tabla.getValueAt(fila, 2).toString().replace(".", ",") );
            txtNota2.setText( tabla.getValueAt(fila, 3).toString().replace(".", ",") );
        }        
        
    }//GEN-LAST:event_tablaMouseClicked

    private void cmdNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdNotaActionPerformed
        if(fila!=-1){
            //nota final = nota1 * 50%  + nota * %50
            float notafinal = (Float.valueOf( txtNota1.getText().replace(",", ".") )*0.5F) + (Float.valueOf( txtNota2.getText().replace(",", ".") )*0.5F) ;
            //examen1=?, examen2=? notafinal, m_cu=? ,  m_as=? , m_gestion=?
            String[] datos = { txtNota1.getText(), txtNota2.getText(), String.valueOf(notafinal).replace(".", ","), this.cu, this.asignatura, this.gestion};    
            sql.setNota(datos);
            Actualizar_Tabla(asignatura,gestion);
        }else{
            JOptionPane.showMessageDialog(this,msg.getError2());
        }
        
}//GEN-LAST:event_cmdNotaActionPerformed

 private void Actualizar_Tabla(String a, String g){                    
        // se colocan los datos en la tabla
        DefaultTableModel datos = sql.getDatosNotas(a, g);
        tabla.setModel(datos);       
}
private void Actualizar_Combos(){      
    dtAs =   sql.getDatosAsignatura2();
    cboAsignatura.removeAllItems();
    for(int i=0; i<=dtAs.length-1;i++){
        cboAsignatura.addItem(dtAs[i].toString());        
    }       
}
    
  private void change(){
        if(cboAsignatura.getSelectedItem()!=null && cboGestion.getSelectedItem()!=null){
            this.asignatura = cboAsignatura.getSelectedItem().toString();
            this.gestion = cboGestion.getSelectedItem().toString();            
            Actualizar_Tabla(asignatura,gestion);
        }      
 }
