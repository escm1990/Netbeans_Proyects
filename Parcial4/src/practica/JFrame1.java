public void guardar() throws IOException{
    try {
        String tabla = null;
        JFileChooser guardar = new JFileChooser();
        guardar.setFileFilter(filter);
        guardar.setCurrentDirectory(null);
        guardar.showSaveDialog(this);
        File Guardar = guardar.getSelectedFile();
        if (Guardar != null){
            nombr = guardar.getSelectedFile().getName();
            FileWriter guard = new FileWriter(Guardar);
            guard.write(jLabelNom.getText());
            guard.write(" " + jLabelApe.getText());
            guard.write(" " + jLabelEdad.getText());
            guard.write(" " + jLabelPais.getText());
            guard.write(" " + jLabelSexo.getText());
     for(int numFila=0; numFila<modelo.getRowCount();numFila++){
         for (int numColum=0; numColum<modelo.getColumnCount(); numColum++){
             tabla = modelo.getValueAt(numFila,numColum).toString();
             guard.flush();
             guard.write(tabla + " ");
         }
         guard.write(" \n ");
         guard.write(" \n ");
     }
            guard.close();
        }
    } catch (IOException ioe) {
    System.out.println(ioe);
    }
}

